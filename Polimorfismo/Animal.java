package Polimorfismo;

public class Animal {
    //Atributos
    private String name;
    private String type;

    //Metodos
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }
    
}